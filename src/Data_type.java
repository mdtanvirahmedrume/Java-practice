public class Data_type {
    public static void main(String[] args) {
        int a = 5;
        int b = 4;
        //increment
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a);

        //Decrement
        System.out.println(b--);
        System.out.println(b);
        System.out.println(--b);
        System.out.println(b);
    }
}
